#capitalize()-first letter capital krega bkki sabh small bhle space ho ya nhi
demo='hello world'
print("string is:",demo)
print("after string capitalisation:",demo.capitalize())
print('-----------------------------------------')

#lower()-converts all the letters of the string to lower case
demo='HELLO WORLD'
print("string is :",demo)
print("Lower case:",demo.lower())
print('-----------------------------------------')

#upper()-converts all the letters of the string to upper case
demo='hello world'
print("string is:",demo)
print("Upper case is:",demo.upper())
print('-----------------------------------------')

#title()-title k according set krdeta hai
demo='welcome to python programming'
print('string is:',demo)
print('title format:',demo.title())
print('-----------------------------------------')

#split()-specified letter se split krega
demo='bhavishya'
print("string is:",demo)
print("split at a:",demo.split('a'))
print('-----------------------------------------')

#swapcase()-changes the letters in uppercase to lowercase and vice versa
demo='PytHON'
print('string is:',demo)
print('swapcase is:',demo.swapcase())
print('-----------------------------------------')

#replace()-replaces the letter
demo='Python'
print('string is:',demo)
print('replacing ',demo.replace('h','H'))
print('-----------------------------------------')
